  'overused-font-inter': {
    severity: 'warning', category: 'ai-slop',
    description: 'Inter used as primary font — the default choice of every SaaS template.',
    test(src) {
      if (!/\bInter\b/.test(src)) return [];
      // Suppress if Inter is part of a larger fallback stack with a non-inter lead
      if (/font-family\s*:\s*["']?(?!Inter)[A-Za-z "'-]+,\s*['"a-zA-Z -]*Inter/i.test(src)) return [];
      const snippet = _snippet(src, /\bInter\b/);
      return [{ rule: this.id, severity: this.severity,
        message: 'Inter is the default "AI font". Use the Segoe UI / Aptos stack or a project-specific font.',
        snippet }];
    },
  },

