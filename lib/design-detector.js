// Deterministic design anti-pattern detector.
// Inspired by Impeccable's 46-rule detector (github.com/pbakaus/impeccable).
//
// Scans raw HTML/CSS/JS/TSX/Svelte source for "AI slop" tells and general
// design-quality issues. Runs entirely in-process — no LLM, no API key.
//
// Usage:
//   import { detectDesignIssues, RULES } from './design-detector.js';
//   const { findings } = detectDesignIssues(sourceText, { filename: 'app.html' });

// ── Rule registry ─────────────────────────────────────────────────────────

/**
 * @typedef {{ id: string, severity: 'error'|'warning'|'info', category: 'ai-slop'|'quality'|'accessibility', description: string }} RuleMeta
 * @typedef {{ rule: string, severity: string, message: string, snippet?: string }} Finding
 */

/** @type {Record<string, RuleMeta & { test: (src: string, filename?: string) => Finding[] }>} */
