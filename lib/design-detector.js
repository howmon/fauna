// ── Helpers ────────────────────────────────────────────────────────────────

function _snippet(src, re) {
  const m = src.match(re);
  if (!m) return undefined;
  return String(m[0]).slice(0, 100).replace(/\s+/g, ' ').trim();
}

// Attach rule id to each rule entry (so rules can self-reference)
for (const [id, rule] of Object.entries(RULES)) rule.id = id;

// ── Public API ─────────────────────────────────────────────────────────────

/**
 * Run all enabled rules on a source string.
 * @param {string} source   Raw file content
 * @param {{ filename?: string, disabledRules?: string[], categoryFilter?: string[] }} opts
 * @returns {{ findings: Finding[], summary: string }}
 */
export function detectDesignIssues(source, opts = {}) {
  const { filename = '', disabledRules = [], categoryFilter = null } = opts;
  const findings = [];

  for (const [id, rule] of Object.entries(RULES)) {
    if (disabledRules.includes(id)) continue;
    if (categoryFilter && !categoryFilter.includes(rule.category)) continue;
    try {
      const hits = rule.test(source, filename);
      findings.push(...hits);
    } catch (_) { /* never let a rule crash the caller */ }
  }

  const errors   = findings.filter(f => f.severity === 'error').length;
  const warnings = findings.filter(f => f.severity === 'warning').length;
  const infos    = findings.filter(f => f.severity === 'info').length;
  const parts = [];
  if (errors)   parts.push(errors   + ' error'   + (errors   > 1 ? 's' : ''));
  if (warnings) parts.push(warnings + ' warning' + (warnings > 1 ? 's' : ''));
  if (infos)    parts.push(infos    + ' hint'    + (infos    > 1 ? 's' : ''));

  return {
    findings,
    summary: findings.length ? parts.join(', ') : 'no issues found',
  };
}

/**
 * Quick check — returns true if any finding of the given severity (or higher) exists.
 * Severity order: error > warning > info
 */
export function hasDesignIssues(source, opts = {}) {
  const { minSeverity = 'warning' } = opts;
  const { findings } = detectDesignIssues(source, opts);
  const allowed = minSeverity === 'error' ? ['error'] :
                  minSeverity === 'warning' ? ['error', 'warning'] : ['error', 'warning', 'info'];
  return findings.some(f => allowed.includes(f.severity));
}

/** Format findings as a compact human-readable block for tool output. */
export function formatFindings(findings, filename = '') {
  if (!findings.length) return '';
  const header = filename ? `Design findings for ${filename}:` : 'Design findings:';
  const lines = findings.map(f => `  [${f.severity.toUpperCase()}] ${f.rule}: ${f.message}${f.snippet ? ` (e.g. \`${f.snippet}\`)` : ''}`);
  return [header, ...lines].join('\n');
}
