// ── System context (loaded once at startup) ───────────────────────────────
var sysCtx = {};
var lastPermState = {};

async function loadSysCtx() {
  try {
    var r = await fetch('/api/system-context');
    var d = await r.json();
    sysCtx = d;
    lastPermState = d.permissions || {};
  } catch (_) {}
}

